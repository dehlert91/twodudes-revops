         SELECT le.po_number,
            round(sum(le.paid_hours), 2) AS hours_to_date,
            round(sum(le.paid_hours * COALESCE(p.specialty_rate, le.hourly_rate)), 2) AS ct_labor_cost_to_date
           FROM labor_entries le
             LEFT JOIN projects p ON p.po_number = le.po_number::text
          WHERE le.po_number IS NOT NULL
          GROUP BY le.po_number
