                    WHEN t.billing_schedule_type = 'progress_only'::text THEN
                    CASE
                        WHEN t.stage = ANY (ARRAY['Need to Invoice'::text, 'Benchmark in Progress'::text, 'Benchmark Completed'::text]) THEN 'Final'::text
                        ELSE 'Progress'::text
                    END
                    WHEN t.billing_schedule_type = 'wip_then_final'::text THEN
                    CASE
                        WHEN t.final_count = 0 THEN 'Final'::text
                        ELSE NULL::text
                    END
                    WHEN t.billing_schedule_type = 'wip_then_progress'::text THEN
                    CASE
                        WHEN t.stage = ANY (ARRAY['Need to Invoice'::text, 'Benchmark in Progress'::text, 'Benchmark Completed'::text]) THEN 'Final'::text
                        ELSE 'Progress'::text
                    END
                    ELSE NULL::text
                END AS expected_next_invoice_type,
                CASE
                    WHEN t.schedule_has_wip_accrual = true AND t.costs_in_excess_of_billings_calc > 0::numeric AND t.is_closed = false THEN true
                    ELSE false
                END AS wip_eligible,
                CASE
                    WHEN t.is_closed THEN 'closed'::text
                    WHEN t.billing_schedule_type IS NULL THEN 'unassigned'::text
                    WHEN t.billings_in_excess_of_costs_calc > 0::numeric THEN 'overbilled'::text
                    WHEN t.schedule_has_deposit = true AND t.deposit_count = 0 THEN 'awaiting_deposit'::text
                    WHEN (t.stage = ANY (ARRAY['Need to Invoice'::text, 'Benchmark in Progress'::text, 'Benchmark Completed'::text])) AND t.final_count = 0 THEN 'awaiting_final'::text
                    WHEN t.unbilled_revenue_calc > 0::numeric THEN 'behind'::text
                    ELSE 'on_track'::text
                END AS billing_status
           FROM tier7 t
        )
