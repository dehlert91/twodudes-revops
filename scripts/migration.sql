          GROUP BY qbo_expenses.po_number
        ), set_rollup AS (
         SELECT qbo_expenses.po_number,
            round(sum(qbo_expenses.amount), 2) AS set_cost_to_date
           FROM qbo_expenses
          WHERE (qbo_expenses.account_name::text = ANY (ARRAY['5040 Sub-Contractors'::text, '5070 Fld Equip, Rent, Repair, Supp'::text, '5080 Job Related Travel'::text, '6230 Fld Equip, Rent, Repair, Supp:Rental Equipment for Job'::text, '6243 Fld Equip, Rent, Repair, Supp:Equipment Repairs'::text, '6303 Job Related Travel:Tolls & Parking'::text, 'Fld Equip, Rent, Repair, Supp:Rental Equipment for Job'::text, 'Job Related Travel'::text, 'Job Related Travel:Mileage Reimbursement'::text, 'Job Related Travel:Tolls & Parking'::text, 'Sub-Contractors'::text])) AND qbo_expenses.po_number IS NOT NULL
          GROUP BY qbo_expenses.po_number
