         SELECT qbo_invoices.po_number,
            round(sum(qbo_invoices.total_amount), 2) AS amount_billed_to_date,
            count(*) AS invoice_count,
            max(qbo_invoices.invoice_date) AS last_invoice_date,
            round(sum(
                CASE
                    WHEN qbo_invoices.milestone_type::text = 'Deposit'::text THEN qbo_invoices.total_amount
                    ELSE 0::numeric
                END), 2) AS deposit_billed,
            round(sum(
                CASE
                    WHEN qbo_invoices.milestone_type::text = 'Progress'::text THEN qbo_invoices.total_amount
                    ELSE 0::numeric
                END), 2) AS progress_billed,
            round(sum(
                CASE
                    WHEN qbo_invoices.milestone_type::text = 'Final'::text THEN qbo_invoices.total_amount
                    ELSE 0::numeric
